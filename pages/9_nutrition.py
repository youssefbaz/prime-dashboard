    try:
        r = requests.post(
            f"https://generativelanguage.googleapis.com/v1/models/gemini-2.0-flash:generateContent?key={gemini_key}",
            headers={"Content-Type": "application/json"},
            json={
                "contents": [{"parts": [{"text": prompt}]}],
                "generationConfig": {"temperature": 0.7, "maxOutputTokens": 4000},
            },
            timeout=60,
        )
        if r.status_code == 200:
            content = r.json()["candidates"][0]["content"]["parts"][0]["text"]
            content = re.sub(r"```json|```", "", content).strip()
            start = content.find("{")
            end   = content.rfind("}") + 1
            plan  = json.loads(content[start:end])
            # Enrich each meal with a TheMealDB image
            for meal in plan.get("meals", []):
                meal["image_url"] = fetch_mealdb_image(meal.get("name", ""))
            return plan
        else:
            return {"error": f"API error {r.status_code}: {r.text[:300]}"}
    except json.JSONDecodeError as e:
        return {"error": f"JSON parse error: {e}"}
    except Exception as e:
        return {"error": str(e)}

